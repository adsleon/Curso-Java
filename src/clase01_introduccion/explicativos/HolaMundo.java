package clase01_introduccion.explicativos;

/**
 * Ejemplo básico en Java.
 * ----------------------------------
 * En este archivo aprenderás:
 * 1. Cómo se define una clase pública.
 * 2. Qué es el método main().
 * 3. Cómo imprimir mensajes en consola.
 *
 * NOTA:
 * - La clase debe llamarse igual que el archivo: HolaMundo.java
 * - El método main() es el punto de entrada de cualquier programa en Java.
 */
public class HolaMundo {
    public static void main(String[] args) {
        // Imprimimos un mensaje en la consola
        System.out.println("¡Hola Mundo desde Java!");

        // Podemos imprimir más de una línea
        System.out.println("Este es tu primer programa en Java.");
    }
}
